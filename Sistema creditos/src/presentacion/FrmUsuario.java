    private void btnEditarCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarCatActionPerformed
        if (tblListCat.getSelectedRowCount() == 1) {
            String idUsuario = String.valueOf(tblListCat.getValueAt(tblListCat.getSelectedRow(), 0));
            String usuario = String.valueOf(tblListCat.getValueAt(tblListCat.getSelectedRow(), 1));
            String nombreApe = String.valueOf(tblListCat.getValueAt(tblListCat.getSelectedRow(), 2));
            String clave = String.valueOf(tblListCat.getValueAt(tblListCat.getSelectedRow(), 3));
            

            txtId.setText(idUsuario);
            txtUsuario.setText(usuario);
            txtNombreApe.setText(nombreApe);
            txtPassword.setText(clave);
            txtConfirmPass.setText(clave);
            btnGuardar.setText("Editar");
            panelCategoria.setEnabledAt(0, false);
            panelCategoria.setEnabledAt(1, true);
            panelCategoria.setSelectedIndex(1);
            this.accion = "editar";
        } else {
            this.mjeError("Debe seleccionar un registro");
        }
    }//GEN-LAST:event_btnEditarCatActionPerformed

