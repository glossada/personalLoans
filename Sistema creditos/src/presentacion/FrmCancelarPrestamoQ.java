    private double calcularValotCancelar(int idPrestamo){
        int cantCuotas=CTRL.cantCuotas(idPrestamo);
        int cantCuotasPagadas=CTRL.cantCuotasPagadas(idPrestamo);
        double monto = CTRL.montoPrestado(idPrestamo);
        double total=0;
        double interes=CTRL.getInteres(idPrestamo)/100;
        if(interes==0){
            interes=0.11;
        }
        for (int i = 0; i <= cantCuotasPagadas; i++) {
            total+=((monto/(cantCuotasPagadas+1))-(monto/cantCuotas))*Math.pow((1+interes),i);
            
        }
        total+=CTRL.valorCuota(idPrestamo, cantCuotasPagadas+1);
        return Math.ceil(total);
    }
    
