    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double totalPagado = Double.parseDouble(txtValorCancelar.getText()) + CTRL.sumarCuotasEstado(this.idPrestamo, "Pagado");
        double gananciaTotal=totalPagado - CTRL.montoPrestado(idPrestamo);
        double gananciaCancel=gananciaTotal-CTRL.sumarGananciaEstado(idPrestamo, "Pagado");
        String fecha=this.fechaFin;
        String[] part=fecha.split("/");
        String dia = part[0];
        String mes = part[1];
        String año = part[2];
        CTRL.setFechaFinPrestamoCancelar(this.fechaFin, this.idPrestamo);
        CTRL.setEstadoPrestamo("Finalizado", this.idPrestamo);
        CTRLM.insertar(this.idCliente, this.cliente, "Prestamo Cancelado", Double.parseDouble(txtValorCancelar.getText()), gananciaCancel, dia, mes, año, "E");
        String resp = CTRL.setTotalPagar(this.idPrestamo, totalPagado);
        String resp2 = CTRL.editarCuotaEstado("Pagado", this.idPrestamo, "a pagar");
        String resp3 = CTRL.setProxCuota("1", this.idPrestamo);
        if (resp.equals("OK") && resp2.equals("OK") && resp3.equals("OK")) {
            this.mjeOK("Prestamo cancelado por monto " + txtValorCancelar.getText());
            this.setVisible(false);
        } else {
            this.mjeError("error al cancelar prestamo");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

