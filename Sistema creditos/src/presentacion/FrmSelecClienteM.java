    private void btnEditarCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarCatActionPerformed
        if (tblListCat.getSelectedRowCount() == 1) {
            String idCliente = String.valueOf(tblListCat.getValueAt(tblListCat.getSelectedRow(), 0));
            String Nombre = String.valueOf(tblListCat.getValueAt(tblListCat.getSelectedRow(), 1));
            String Edad = String.valueOf(tblListCat.getValueAt(tblListCat.getSelectedRow(), 2));
            String Cuit = String.valueOf(tblListCat.getValueAt(tblListCat.getSelectedRow(), 3));
            String Direccion = String.valueOf(tblListCat.getValueAt(tblListCat.getSelectedRow(), 4));
            String Email = String.valueOf(tblListCat.getValueAt(tblListCat.getSelectedRow(), 5));

            txtId.setText(idCliente);
            txtNombre.setText(Nombre);
            txtEdad.setText(Edad);
            txtCuit.setText(Cuit);
            txtDireccion.setText(Direccion);
            txtEmail.setText(Email);
            btnGuardar.setText("Editar");
            panelCategoria.setEnabledAt(0, false);
            panelCategoria.setEnabledAt(1, true);
            panelCategoria.setSelectedIndex(1);
            this.accion = "editar";
        } else {
            this.mjeError("Debe seleccionar un registro");
        }
    }//GEN-LAST:event_btnEditarCatActionPerformed

    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        if (tblListCat.getSelectedRowCount() == 1) {
            String id = String.valueOf(tblListCat.getValueAt(tblListCat.getSelectedRow(), 0));
            String nombre = String.valueOf(tblListCat.getValueAt(tblListCat.getSelectedRow(), 1));

            if (JOptionPane.showConfirmDialog(this, "Deseas eliminar el registro: " + nombre + " ?", "Eliminar", JOptionPane.YES_NO_OPTION) == 0) {
                String resp = this.CTRL.eliminar(Integer.parseInt(id));
                if (resp.equals("OK")) {
                    this.mjeOK("Registro eliminado");
                    this.listar("",false);
                } else {
                    this.mjeError(resp);
                }
            }
        } else {
            this.mjeError("Seleccione 1 registro a desactivar.");
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

