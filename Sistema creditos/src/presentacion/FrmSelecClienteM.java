    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String resp;
        if (this.accion.equals("guardar")) {

            if (txtNombre.getText().length() > 100 || txtNombre.getText().length() == 0) {
                JOptionPane.showMessageDialog(this, "La marca no debe exceder los 20 caracteres y no debe estar vacia", "sistema", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (txtEdad.getText().length() > 4 || txtEdad.getText().length() == 0) {
                JOptionPane.showMessageDialog(this, "Nombre no debe exceder los 20 caracteres y no debe estar vacio", "sistema", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (txtDireccion.getText().length() > 100  || txtDireccion.getText().length() == 0) {
                JOptionPane.showMessageDialog(this, "Cantidad fuera de limites o no se completo el campo", "sistema", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (txtCuit.getText().length() > 45 || txtCuit.getText().length() == 0) {
                JOptionPane.showMessageDialog(this, "Precio fuera de limites o no se completo el campo", "sistema", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (txtEmail.getText().length() > 45 || txtEmail.getText().length() == 0) {
                JOptionPane.showMessageDialog(this, "valor dolar fuera de limites o no se completo el campo", "sistema", JOptionPane.WARNING_MESSAGE);
                return;
            }

            //         int idProd;
            resp = this.CTRL.insertar(txtNombre.getText(), Integer.parseInt(txtEdad.getText()), txtCuit.getText(), txtDireccion.getText(),txtEmail.getText(),txtTel1.getText());
            if (resp.equals("OK")) {
                this.mjeOK("El registro se guardo correctamente");
                this.limpiar();
                panelCategoria.setEnabledAt(1, false);
                panelCategoria.setEnabledAt(0, true);
                panelCategoria.setSelectedIndex(0);
                this.listar("",false);
            } else {
                this.mjeError(resp);

            }
        } else{

            if (txtNombre.getText().length() > 100 || txtNombre.getText().length() == 0) {
                JOptionPane.showMessageDialog(this, "La marca no debe exceder los 20 caracteres y no debe estar vacia", "sistema", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (txtEdad.getText().length() > 4 || txtEdad.getText().length() == 0) {
                JOptionPane.showMessageDialog(this, "Nombre no debe exceder los 20 caracteres y no debe estar vacio", "sistema", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (txtDireccion.getText().length() > 100  || txtDireccion.getText().length() == 0) {
                JOptionPane.showMessageDialog(this, "Cantidad fuera de limites o no se completo el campo", "sistema", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (txtCuit.getText().length() > 45 || txtCuit.getText().length() == 0) {
                JOptionPane.showMessageDialog(this, "Precio fuera de limites o no se completo el campo", "sistema", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (txtEmail.getText().length() > 45 || txtEmail.getText().length() == 0) {
                JOptionPane.showMessageDialog(this, "valor dolar fuera de limites o no se completo el campo", "sistema", JOptionPane.WARNING_MESSAGE);
                return;
            }

            String nomAnt=txtNombre.getText();
            //         int idProd;
            resp = this.CTRL.actualizar(Integer.parseInt(txtId.getText()),txtNombre.getText(), nomAnt, Integer.parseInt(txtEdad.getText()), txtCuit.getText(), txtDireccion.getText(),txtEmail.getText(),txtTel1.getText());
            if (resp.equals("OK")) {
                this.mjeOK("El registro se guardo correctamente");
                this.limpiar();
                panelCategoria.setEnabledAt(1, false);
                panelCategoria.setEnabledAt(0, true);
                panelCategoria.setSelectedIndex(0);
                this.listar("",false);
            } else {
                this.mjeError(resp);

            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

